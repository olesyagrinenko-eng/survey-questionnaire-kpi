def _extra_with_tids(extra: dict) -> dict:
    inj = dict(extra.get("inject") or {})
    tpls = []
    for i, t in enumerate(inj.get("templates") or []):
        tpls.append({**t, "tid": f"{extra['id']}__{i}"})
    inj["templates"] = tpls
    return {**extra, "inject": inj}


