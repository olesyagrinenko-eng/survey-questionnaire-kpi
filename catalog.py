def _with_template_ids(group: dict) -> dict:
    """Добавить уникальный tid каждому шаблону: {group_id}__{n}."""
    gid = group["id"]
    out = {**group, "templates": []}
    for i, t in enumerate(group.get("templates") or []):
        t2 = {**t, "tid": f"{gid}__{i}"}
        out["templates"].append(t2)
    return out


