def group_applies(group, phase: str, active_stimuli: set) -> bool:
    """phase: 'pre' | 'post'; active_stimuli: set of keys e.g. {'video','layout'}."""
    if group["phase"] == "post_only" and phase != "post":
        return False
    fs = set(group.get("for_stimuli") or [])
    if not fs:
        return True
    return bool(fs & active_stimuli)


def collect_templates_for_group(group, active_stimuli: set):
    """Возвращает шаблоны группы, если она применима к набору стимулов."""
    fs = set(group.get("for_stimuli") or [])
    if fs and not (fs & active_stimuli):
        return []
    return list(group.get("templates") or [])
